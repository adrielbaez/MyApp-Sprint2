const mongoose = require('mongoose')

const UserSchema = new mongoose.Schema({
    nickName: {
        type: String,
        required: true,
        unique: true
    },
    firstName: {
        type: String,
        required: true
    },
    lastName: {
        type: String,
        required: true
    },
    email: {
        type: String,
        required: true,
        unique: true
    },
    phone: {
        type: String,
        required: true
    },
    address: {
        type: String,
        required: true
    },
    password: {
        type: String,
        required: true
    },
    isAdmin: {
        type: Boolean,
        default: false
    },
    history: {
        type: Array
    },
})

const UserModel = mongoose.model('user', UserSchema)

module.exports = UserModel;